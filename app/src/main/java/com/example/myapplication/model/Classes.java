package com.example.myapplication.model;

public class Classes {
    private Integer id;
    private String name;

    public Classes() {
    }

    public Classes(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
